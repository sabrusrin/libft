#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int lens1;

	i = 0;
	lens1 = 0;
	while (s1[lens1])
		lens1++;
	while (s2[i])
		s1[lens1++] = s2[i++];
	s1[lens1] = '\0';
	return (s1);
}
